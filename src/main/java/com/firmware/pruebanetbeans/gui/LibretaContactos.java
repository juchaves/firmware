    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        // TODO add your handling code here:
        int fila = jTable1.getSelectedRow();
        Long identificacion = ( Long )jTable1.getModel().getValueAt(fila, 3);
        try {
            ContactoServiceImpl.getInstance().borrar(identificacion);
        } catch( ContactoException e ) {
            JOptionPane.showInternalMessageDialog(this, "Imposible eliminar elemento de la tabla");
        }
        actualizacionTablaConEstado();
    }//GEN-LAST:event_jbEliminarActionPerformed

