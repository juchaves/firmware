    private void actualizacionTablaConEstado() {
        Optional<Long> telefono = Optional.ofNullable(null);
        if( jchFiltroTelefono.isSelected() ) {
        try {
                telefono = Optional.of( Long.parseLong( jtFiltroTelefono.getText() ) );
        } catch( NumberFormatException e ) {}
        }
        Optional<Long> identificacion = Optional.ofNullable(null);
        if( jchFiltroIdentificacion.isSelected() ) {
        try {
                identificacion = Optional.of( Long.parseLong( jtFiltroIdentificacion.getText() ) );
        } catch( NumberFormatException e ) {}
        }
        try {
            jTable1.setModel( new TMListaContactos(ContactoServiceImpl.getInstance().obtener( identificacion, telefono ) ) );
            jTable1.invalidate();
        } catch( ContactoException e ) {
            JOptionPane.showInternalMessageDialog(this.getContentPane(), "Imposible obtener el listado");
        }
    }
    private void jbCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCrearActionPerformed
        // TODO add your handling code here:
        Contacto c = new Contacto( jtNombre.getText(), Long.parseLong(jtTelefono.getText()),
                jtDireccion.getText(), Long.parseLong(jtIdentificacion.getText()) );
        ContactoServiceImpl.getInstance().crear(c);
        actualizacionTablaConEstado();
        
    }//GEN-LAST:event_jbCrearActionPerformed

